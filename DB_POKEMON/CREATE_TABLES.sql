    FOREIGN KEY (FK_POKEMON_ID_POKEMON)
    REFERENCES POKEMON (ID_POKEMON); 
   
 
ALTER TABLE POKEMON_HABILIDADE ADD CONSTRAINT FK_POKEMON_HABILIDADE_1
    FOREIGN KEY (FK_HABILIDADE_ID_HABILIDADE)
    REFERENCES HABILIDADE (ID_HABILIDADE);
    
 
ALTER TABLE POKEMON_HABILIDADE ADD CONSTRAINT FK_POKEMON_HABILIDADE_2
    FOREIGN KEY (FK_POKEMON_ID_POKEMON)
    REFERENCES POKEMON (ID_POKEMON);
    
 
ALTER TABLE POKEMON_EVOLUCAO ADD CONSTRAINT FK_POKEMON_EVOLUCAO_1
    FOREIGN KEY (FK_POKEMON_ID_POKEMON)
    REFERENCES POKEMON (ID_POKEMON)
